import pytest
from tic_tac_toe_checker import is_solved


@pytest.mark.parametrize(
    "board_state, return_value",
    [
        ([[1, 1, 1], [2, 0, 2], [0, 0, 0]], 1),
        ([[0, 0, 2], [1, 1, 1], [0, 2, 0]], 1),
        ([[0, 2, 2], [2, 0, 0], [1, 1, 1]], 1),
        ([[1, 2, 0], [1, 2, 0], [1, 0, 0]], 1),
        ([[2, 1, 0], [0, 1, 0], [0, 1, 2]], 1),
        ([[0, 0, 1], [2, 0, 1], [0, 2, 1]], 1),
        ([[1, 2, 0], [2, 1, 0], [0, 0, 1]], 1),
        ([[2, 0, 1], [0, 1, 2], [1, 0, 0]], 1),
        ([[2, 2, 2], [1, 0, 1], [0, 0, 0]], 2),
        ([[0, 0, 1], [2, 2, 2], [0, 1, 0]], 2),
        ([[0, 1, 0], [1, 0, 1], [2, 2, 2]], 2),
        ([[2, 1, 0], [2, 0, 1], [2, 1, 0]], 2),
        ([[1, 2, 0], [0, 2, 1], [1, 2, 0]], 2),
        ([[1, 0, 2], [0, 1, 2], [0, 0, 2]], 2),
        ([[2, 1, 0], [0, 2, 1], [0, 0, 2]], 2),
        ([[1, 0, 2], [0, 2, 0], [2, 0, 1]], 2),
        ]
    )
def test_is_solved_with_player_victory(
    board_state: list[list],
    return_value: int
    ):
    assert is_solved(board_state) == return_value


